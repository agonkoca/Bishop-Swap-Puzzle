package puzzle.model;

/**
 * Represents the types of bishops in the Bishop Swap Puzzle.
 * <p>
 * This enum defines the possible states of a bishop on the board:
 * NONE - Represents an empty position on the board.
 * BLACK - Represents a black bishop.
 * WHITE - Represents a white bishop.
 * </p>
 */
public enum Bishop {
    /**
     * Represents an empty position on the board.
     */
    NONE,

    /**
     * Represents a black bishop on the board.
     */
    BLACK,

    /**
     * Represents a white bishop on the board.
     */
    WHITE
}
